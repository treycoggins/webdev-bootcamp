// GET requests
// Generally used to request data to view
// Data is sent via query string, as text
// Plainly visible in the URL
// Limited amount of data can be sent


// POST request
// Use to post data to a server
// Used to write/create/update
// Data is sent via request body, not a query string!
// Can send any sort of data (JSON!)



// REST:
    // Representational State Transfer
    // REST is an architectural style for distributed hypermedia systems
    // It is a set of guidelines for how a client and server should communicate and perform CRUD operations on a given source




