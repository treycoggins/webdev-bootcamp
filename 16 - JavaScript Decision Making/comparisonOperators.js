/*
Comparison Operators
  evaluate to true/false when comparing two operands

> greater than
< less than
>= greater than or equal to
<= less than or equal to
== equality (coerces values to be the same type)
!= not equal
=== strict equality (relies on type)
!== strict non-equality

Strings can be evaluated using their ASCII code
*/

null == undefined // true
'1' == 1 // true

// strict equality relies on two operands being the same type

